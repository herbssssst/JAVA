
public class Ex02Week13 {

	public static void main(String[] args) 
	{
		String a = new String (" c#"); //공백포탐
		String b = new String(",c++"); 
		System.out.println(a.length()+":"+b.length()); //길이 함수
		
		a = a.concat(b); //문자열 연결 >> a에 b를 넣음
		System.out.println(a);
		
		a = a.trim(); //좌우 공백 제거? 공백 제거
		System.out.println(a);
		
		a = a.replace("c#","java" ); //a를 만나면 b로 바뀜 >> 문자열 대치
		System.out.println(a);
		
		String[] s = a.split(","); //문자열 분리
		for(int i=0; i<s.length; i++)
			System.out.println("s["+i+"]="+s[i]);
		
		a = a.substring(5); //인덱스 5부터 끝까지 서브 스트링 리턴
		System.out.println(a);
		
		char c = a.charAt(1);
		System.out.println(c);
	}
}
